<script lang="ts">
import { Options, Vue } from "vue-class-component";

@Options({
  name: "NotFound",
})
export default class NotFound extends Vue {
  goHome(): void {
    this.$router.push('/');
  }
  
  goBack(): void {
    if (window.history.length > 1) {
      this.$router.go(-1);
    } else {
      this.$router.push('/');
    }
  }
  
  goExtensions(): void {
    this.$router.push('/extensions');
  }
}
</script>

